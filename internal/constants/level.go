import "sort"

func GetLevel(totalScore int64) float64 {
	if totalScore <= 0 {
		return 0
	}
	if totalScore >= NextLevel[99] {
		return float64(100 + (totalScore-NextLevel[99])/100_000_000_000)
	}

	index := sort.Search(len(NextLevel), func(i int) bool {
		return LevelPrefix[i+1] >= totalScore
	})
	score := LevelPrefix[index]

	return float64(index+1) + float64(totalScore-score)/float64(NextLevel[index])
}

